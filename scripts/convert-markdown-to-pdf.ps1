}

$MarkdownPath = (Resolve-Path -LiteralPath $MarkdownPath).Path
$PdfPath = [System.IO.Path]::GetFullPath($PdfPath)
$pdfDir = Split-Path -Parent $PdfPath
if (-not (Test-Path -LiteralPath $pdfDir)) {
    New-Item -ItemType Directory -Path $pdfDir -Force | Out-Null
}

$bodyHtmlPath = Join-Path $env:TEMP ("git-report-body-" + [Guid]::NewGuid().ToString("n") + ".html")
$fullHtmlPath = Join-Path $env:TEMP ("git-report-" + [Guid]::NewGuid().ToString("n") + ".html")

try {
    $bodyHtml = $null
    $pandoc = Get-Command pandoc -ErrorAction SilentlyContinue
    if ($pandoc) {
        & pandoc $MarkdownPath -f markdown -t html -o $bodyHtmlPath
        if ($LASTEXITCODE -ne 0) { throw "pandoc failed with exit code $LASTEXITCODE" }
        $bodyHtml = Get-Content -LiteralPath $bodyHtmlPath -Raw -Encoding UTF8
    }
    else {
        $escaped = [System.Net.WebUtility]::HtmlEncode((Get-Content -LiteralPath $MarkdownPath -Raw -Encoding UTF8))
        $bodyHtml = "<pre>$escaped</pre>"
    }

    $resolvedLogo = $null
    if ($LogoPath -and (Test-Path -LiteralPath $LogoPath)) {
        $resolvedLogo = (Resolve-Path -LiteralPath $LogoPath).Path
    }

    $html = Build-ReportHtml -BodyHtml $bodyHtml -HeaderText $HeaderLabel -ResolvedLogoPath $resolvedLogo
    Set-Content -LiteralPath $fullHtmlPath -Value $html -Encoding UTF8

    $edge = Get-EdgePath
    if (-not $edge) { throw "Microsoft Edge not found in standard install locations." }

    if (Test-Path -LiteralPath $PdfPath) {
        Remove-Item -LiteralPath $PdfPath -Force
    }

    $uri = ([System.Uri]$fullHtmlPath).AbsoluteUri
    $args = @(
        "--headless",
        "--disable-gpu",
        "--no-pdf-header-footer",
        "--print-to-pdf=$PdfPath",
        $uri
    )
    $p = Start-Process -FilePath $edge -ArgumentList $args -Wait -PassThru -NoNewWindow
    if ($p.ExitCode -ne 0) { throw "Edge print-to-pdf exited with code $($p.ExitCode)" }
    if (-not (Test-Path -LiteralPath $PdfPath)) { throw "PDF was not created at $PdfPath" }
}
finally {
    if (Test-Path -LiteralPath $bodyHtmlPath) { Remove-Item -LiteralPath $bodyHtmlPath -Force -ErrorAction SilentlyContinue }
    if (Test-Path -LiteralPath $fullHtmlPath) { Remove-Item -LiteralPath $fullHtmlPath -Force -ErrorAction SilentlyContinue }
}

Write-Host "Wrote PDF: $PdfPath"

