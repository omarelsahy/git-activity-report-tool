#Requires -Version 5.1
<#
.SYNOPSIS
  Convert markdown to a styled PDF via Microsoft Edge headless printing.

.PARAMETER MarkdownPath
  Absolute or relative path to the markdown file.

.PARAMETER PdfPath
  Absolute or relative output path for the PDF.

.PARAMETER HeaderLabel
  Optional short label shown in the report header.

.PARAMETER LogoPath
  Optional path to a logo image (svg/png/jpg) to show in the header.
#>
param(
    [Parameter(Mandatory = $true)]
    [string] $MarkdownPath,

    [Parameter(Mandatory = $true)]
    [string] $PdfPath,

    [Parameter(Mandatory = $false)]
    [string] $HeaderLabel = "Git Activity Report",

    [Parameter(Mandatory = $false)]
    [string] $LogoPath
)

$ErrorActionPreference = "Stop"

function Get-EdgePath {
    $candidates = @(
        "${env:ProgramFiles}\Microsoft\Edge\Application\msedge.exe",
        "${env:ProgramFiles(x86)}\Microsoft\Edge\Application\msedge.exe"
    )
    foreach ($p in $candidates) {
        if (Test-Path -LiteralPath $p) { return $p }
    }
    return $null
}

function Build-ReportHtml {
    param(
        [string] $BodyHtml,
        [string] $HeaderText,
        [string] $ResolvedLogoPath
    )
    $logoTag = ""
    if ($ResolvedLogoPath) {
        $logoUri = ([System.Uri]$ResolvedLogoPath).AbsoluteUri
        $logoTag = "<img class=""brand-mark"" src=""$logoUri"" alt=""Logo""/>"
    }

    @"
